
package AlmacenBibliografico;


public class revistaCientifica extends autor{
    String nombreRcientifica;
    String editor;
    String añoDeInicioPublicacion;
    String frecuencia;
    String temas;
    String rangoPaginas;
    String añoPublicacion;

    public String getNombreRcientifica() {
        return nombreRcientifica;
    }

    public String getEditor() {
        return editor;
    }

    public String getAñoDeInicioPublicacion() {
        return añoDeInicioPublicacion;
    }

    public String getFrecuencia() {
        return frecuencia;
    }

    public String getTemas() {
        return temas;
    }

    public String getRangoPaginas() {
        return rangoPaginas;
    }

    public String getAñoPublicacion() {
        return añoPublicacion;
    }

    public void setNombreRcientifica(String nombreRcientifica) {
        this.nombreRcientifica = nombreRcientifica;
    }

    public void setEditor(String editor) {
        this.editor = editor;
    }

    public void setAñoDeInicioPublicacion(String añoDeInicioPublicacion) {
        this.añoDeInicioPublicacion = añoDeInicioPublicacion;
    }

    public void setFrecuencia(String frecuencia) {
        this.frecuencia = frecuencia;
    }

    public void setTemas(String temas) {
        this.temas = temas;
    }

    public void setRangoPaginas(String rangoPaginas) {
        this.rangoPaginas = rangoPaginas;
    }

    public void setAñoPublicacion(String añoPublicacion) {
        this.añoPublicacion = añoPublicacion;
    }
    
    
    
}
